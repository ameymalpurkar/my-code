
import turtle

t = turtle.Turtle()
t.speed(1000)
turtle.bgcolor("black")
for i in range(1000):
    t.color("blue")
    t.circle(i)
    t.right(90) 
    
t = turtle.Turtle()
t.speed(1000)
for p in range(1000):
    t.color("red")    
    t.circle(p)
    t.left(90)
    
    
    
    
turtle.done
